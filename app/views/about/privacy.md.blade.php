## PatchNotes Privacy Policy
Last updated November 7th, 2013

This Privacy Policy ("Policy") explains how your personal information is
collected, used and disclosed by Axxim and its subsidiaries and
affiliated companies ("Axxim"). This Policy applies to the websites of
Axxim, including http://patchnotes.net/ ("Sites") and Axxim's other
products, services or any other features, technologies or functionalities
offered by us on our Sites or through any other means (collectively, "Services").
This Policy does not apply to information you provide directly to third party
websites or third party applications that use Axxim's Services. We do not
control the activities of such third parties and advise you to consult their
privacy policies before you share your personal information.

### Section A: Personal Information Collected
We collect information about you in various ways when you use our Sites and
Services. For example, we collect information you provide to us when you
register for a PatchNotes account, create a service or subscription, request customer
service or respond to a PatchNotes survey. Such personal information may include
your name, address, email address, and your IP address. Finally, we may obtain
information about you from other sources, including third party websites or
applications that offer the PatchNotes Services, and combine that with
information we collect on our Sites and through our Services.

#### Subsection 1: IP Addresses
When you visit our Sites or use our Services, some information is
automatically collected. For example, when you visit our Sites, your computer's
operating system, Internet Protocol ("IP") address, access times, browser type
and language and the website you visited before our Sites are logged
automatically. We also collect information about your usage and activity on our
Sites and Services.

#### Subsection 2: Cookies
We automatically collect information using "cookies."
Cookies are small data files stored on your hard drive by a website.  Among
other things, cookies help us improve our Sites, Services and your experience.
We use browser cookies to see which areas and features are popular and to count
visits to our Sites.


